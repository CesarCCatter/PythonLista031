'''
2)	Elaborar um programa que pergunte quatro valores inteiros e apresente 2 resultados:
a) Resultado de suas adições
b) Resultado de suas multiplicações
'''

numero1 = int(input("Escolha um número intero:"))
numero2 = int(input("Escolha outro número intero:"))
numero3 = int(input("Escolha outro número intero:"))
numero4 = int(input("Escolha outro número intero:"))

print("A soma de todos os números escolhidos é:",numero1 + numero2 + numero3 + numero4)
print("O resultado da multiplicação de todos os números escolhidos é:",numero1 * numero2 * numero3 * numero4)